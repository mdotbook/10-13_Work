package Review_practice_activities_10_13_page1;

public class CounterTask3_Evens2to10
{
    public static void main(String[] args) {
        int counter = 2; // (1) start at 2
        while (counter <= 10) { // (2) loop until 10
            System.out.println("Even number = " + counter); // (3) print even
            counter += 2; // (4) increase by 2
        }
        // (5) done

    }
}
