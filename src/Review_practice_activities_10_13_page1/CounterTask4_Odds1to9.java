package Review_practice_activities_10_13_page1;

public class CounterTask4_Odds1to9
{
    public static void main(String[] args) {
        int counter = 1; // (1) start at 1
        while (counter <= 9) { // (2) loop until 9
            System.out.println("Odd number = " + counter); // (3) print odd
            counter += 2; // (4) increase by 2
        }
        // (5) finished


    }
}
