package Review_practice_activities_10_13_page1;

public class CounterTask1_Counter1to5
{
    public static void main(String[] args) {


        int counter = 1; // (1) start counter at 1
        while (counter <= 5) { // (2) loop until counter is 5
            System.out.println("Counter = " + counter); // (3) display counter
            counter++; // (4) increase counter by 1
        }
        // (5) done counting
    }
}


