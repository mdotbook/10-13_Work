package Review_practice_activities_10_13_page1;

public class CounterTask2_CountDown5to1
{
    public static void main(String[] args) {
        int counter = 5; // (1) start at 5
        while (counter >= 1) { // (2) loop until 1
            System.out.println("Counter = " + counter); // (3) show value
            counter--; // (4) subtract 1
        }
        // (5) finished

    }
}
