public class Task2_CountBackward
{
    public static void main(String[] args) {
        // (1) Loop backward from 5 down to 1
        for (int i = 5; i >= 1; i--) {
            System.out.println(i); // (2) Print the current number
        }
        // (3) End of program

    }
}
