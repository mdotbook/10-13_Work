package Review_practice_activities_10_13_page2;

public class Task5_SumOfNumbers
{
    public static void main(String[] args) {
        int sum = 0; // (1) Start with zero
        for (int i = 1; i <= 5; i++) {
            sum += i; // (2) Add each number
        }
        System.out.println("Sum = " + sum); // (3) Show result

    }
}
